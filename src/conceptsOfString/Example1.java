package conceptsOfString;

public class Example1 {

	public static void main(String[] args) {
		String s="My name is Aditya Pandurang Sawant";
		//To count the length of the string
		int l=s.length();
		System.out.println(l);
		// to declare all the character of string in upper case
		String u=s.toUpperCase();
		System.out.println(u);
		// to declare all the character of string in Lower case
		String j=s.toLowerCase();
		System.out.println(j);

	}

}
